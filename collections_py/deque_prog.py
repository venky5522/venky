from collections import deque
d = deque()
print(d)  #deque([])
d.append(10)  #deque([10])
d.append(20)  #deque([10,20])
d.appendleft(30)  #deque([30,10,20])
d.appendleft(40)  #deque([40,30,10,20])
d.extend("python") #deque([40, 30, 10, 20, 'p', 'y', 't', 'h', 'o', 'n'])
d.extendleft("hello") #deque(['o', 'l', 'l', 'e', 'h', 40, 30, 10, 20, 'p', 'y', 't', 'h', 'o', 'n'])
d.pop()  #deque(['o', 'l', 'l', 'e', 'h', 40, 30, 10, 20, 'p', 'y', 't', 'h', 'o'])
d.popleft() #deque(['l', 'l', 'e', 'h', 40, 30, 10, 20, 'p', 'y', 't', 'h', 'o'])
d.rotate()  #deque(['o', 'l', 'l', 'e', 'h', 40, 30, 10, 20, 'p', 'y', 't', 'h'])
d.rotate(-2)  #deque(['l', 'e', 'h', 40, 30, 10, 20, 'p', 'y', 't', 'h', 'o', 'l'])
print(d)
